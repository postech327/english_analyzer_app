    return pw.Container(
      width: double.infinity,
      padding: const pw.EdgeInsets.all(9),
      decoration: pw.BoxDecoration(
        color: PdfColor.fromHex('#EFF6FF'),
        borderRadius: pw.BorderRadius.circular(8),
        border: pw.Border.all(color: PdfColor.fromHex('#93C5FD')),
      ),
      child: pw.Column(
        crossAxisAlignment: pw.CrossAxisAlignment.start,
        children: [
          _smallLabel('문제화 포인트'),
          pw.SizedBox(height: 3),
          pw.Text(
            value,
            style: pw.TextStyle(
              fontSize: 8.5,
              height: 1.4,
              color: PdfColor.fromHex('#1F2937'),
            ),
          ),
        ],
      ),
    );
  }

  static pw.Widget _fullBracketedSection(String value) {
    return pw.Column(
      crossAxisAlignment: pw.CrossAxisAlignment.start,
      children: [
        _sectionTitle('전체 괄호 구조 보기'),
        pw.SizedBox(height: 8),
        pw.Container(
          width: double.infinity,
          padding: const pw.EdgeInsets.all(12),
          decoration: pw.BoxDecoration(
            color: PdfColor.fromHex('#F8FAFC'),
            borderRadius: pw.BorderRadius.circular(10),
            border: pw.Border.all(color: PdfColor.fromHex('#E2E8F0')),
          ),
          child: _bracketedText(
            value,
            style: pw.TextStyle(
              fontSize: 9.6,
              height: 1.6,
              color: PdfColor.fromHex('#111827'),
            ),
          ),
        ),
      ],
    );
  }

  static pw.Widget _bracketedText(
    String value, {
    required pw.TextStyle style,
  }) {
    final spans = <pw.InlineSpan>[];
    var index = 0;

    while (index < value.length) {
      final next = _nextBracket(value, index);
      if (next == null) {
        spans.add(pw.TextSpan(text: value.substring(index), style: style));
        break;
      }

      if (next.start > index) {
        spans.add(pw.TextSpan(
            text: value.substring(index, next.start), style: style));
      }

      final end = value.indexOf(next.close, next.start + 1);
      if (end == -1) {
        spans.add(pw.TextSpan(text: value.substring(next.start), style: style));
        break;
      }

      final token = value.substring(next.start, end + 1);
      final color = _bracketColor(next.open, token);
      spans.add(
        pw.TextSpan(
          text: token,
          style: style.copyWith(
            color: color,
            fontWeight: pw.FontWeight.bold,
          ),
        ),
      );
      index = end + 1;
    }

    return pw.RichText(text: pw.TextSpan(children: spans, style: style));
  }

  static ({int start, String open, String close})? _nextBracket(
    String value,
    int from,
  ) {
    final candidates = <({int start, String open, String close})>[
      (start: value.indexOf('[', from), open: '[', close: ']'),
      (start: value.indexOf('(', from), open: '(', close: ')'),
      (start: value.indexOf('{', from), open: '{', close: '}'),
    ].where((item) => item.start >= 0).toList();
    if (candidates.isEmpty) return null;
    candidates.sort((a, b) => a.start.compareTo(b.start));
    return candidates.first;
  }

  static PdfColor _bracketColor(String open, String token) {
    if (open == '(') return PdfColor.fromHex('#0F766E');
    if (open == '{') return PdfColor.fromHex('#EA580C');
    return PdfColor.fromHex('#2563EB');
  }

  static pw.Widget _sectionBox({
    required String title,
    required String color,
    required String border,
    required pw.Widget child,
  }) {
    return pw.Container(
      width: double.infinity,
      padding: const pw.EdgeInsets.all(12),
      decoration: pw.BoxDecoration(
        color: PdfColor.fromHex(color),
        borderRadius: pw.BorderRadius.circular(10),
        border: pw.Border.all(color: PdfColor.fromHex(border)),
      ),
      child: pw.Column(
        crossAxisAlignment: pw.CrossAxisAlignment.start,
        children: [
          _sectionTitle(title),
          pw.SizedBox(height: 8),
          child,
        ],
      ),
    );
  }

  static pw.Widget _sectionTitle(String title) {
    return pw.Text(
      title,
      style: pw.TextStyle(
        fontSize: 13,
        fontWeight: pw.FontWeight.bold,
        color: PdfColor.fromHex('#172033'),
      ),
    );
  }

  static pw.Widget _smallLabel(String label, {String color = '#2563EB'}) {
    return pw.Text(
      label,
      style: pw.TextStyle(
        fontSize: 8.5,
        fontWeight: pw.FontWeight.bold,
        color: PdfColor.fromHex(color),
      ),
    );
  }

  static pw.Widget _emptyText(String value) {
    return pw.Text(
      value,
      style: const pw.TextStyle(fontSize: 9, color: PdfColors.grey700),
    );
  }

  static String _filename(FinalTouchReport report) {
    final parts = [
      'final_touch',
      if (report.textbookFolder.trim().isNotEmpty) report.textbookFolder,
      if (report.unitFolder.trim().isNotEmpty) report.unitFolder,
      if (report.textbookFolder.trim().isEmpty &&
          report.unitFolder.trim().isEmpty &&
          report.source.trim().isNotEmpty)
        report.source,
      _dateForFilename(),
    ];
    return '${parts.map(_safePart).where((part) => part.isNotEmpty).join('_')}.pdf';
  }

  static String _safePart(String value) {
    return value
        .replaceAll(RegExp(r'[\\/:*?"<>|]'), '')
        .replaceAll(RegExp(r'\s+'), '')
        .trim();
  }

  static String _dateForFilename() {
    final now = DateTime.now();
    return '${now.year}-${now.month.toString().padLeft(2, '0')}-'
        '${now.day.toString().padLeft(2, '0')}';
  }

  static String _formatDate(String value) {
    final parsed = DateTime.tryParse(value);
    final date = parsed?.toLocal() ?? DateTime.now();
    return '${date.year}.${date.month.toString().padLeft(2, '0')}.'
        '${date.day.toString().padLeft(2, '0')}';
  }
}
